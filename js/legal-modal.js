document.addEventListener('DOMContentLoaded', () => {
  let modalEl = null;

  const ensureModal = () => {
    if (modalEl) return modalEl;

    modalEl = document.createElement('div');
    modalEl.className = 'legal-modal';
    modalEl.id = 'legalModal';
    modalEl.hidden = true;
    modalEl.innerHTML = `
      <div class="legal-modal__backdrop" data-close-legal></div>
      <div class="legal-modal__dialog" role="dialog" aria-modal="true" aria-labelledby="legalModalTitle">
        <div class="legal-modal__head">
          <h2 class="legal-modal__title" id="legalModalTitle"></h2>
          <button type="button" class="legal-modal__close" data-close-legal aria-label="ปิด">
            <i data-lucide="x" class="icon icon--20" aria-hidden="true"></i>
          </button>
        </div>
        <div class="legal-modal__body" id="legalModalBody"></div>
        <div class="legal-modal__foot">
          <button type="button" class="btn btn--primary legal-modal__btn" data-close-legal>ปิด</button>
        </div>
      </div>
    `;
    document.body.appendChild(modalEl);

    modalEl.querySelectorAll('[data-close-legal]').forEach((el) => {
      el.addEventListener('click', closeLegalModal);
    });

    return modalEl;
  };

  const openLegalModal = (type) => {
    const content = LEGAL_CONTENT[type];
    if (!content) return;

    const modal = ensureModal();
    modal.querySelector('#legalModalTitle').textContent = content.title;
    modal.querySelector('#legalModalBody').innerHTML = content.body;
    modal.hidden = false;
    document.body.style.overflow = 'hidden';
    window.refreshLucideIcons?.(modal);
    modal.querySelector('.legal-modal__close')?.focus();
  };

  const closeLegalModal = () => {
    if (!modalEl || modalEl.hidden) return;
    modalEl.hidden = true;
    document.body.style.overflow = '';
  };

  document.addEventListener('click', (e) => {
    const trigger = e.target.closest('[data-legal]');
    if (!trigger) return;
    e.preventDefault();
    openLegalModal(trigger.dataset.legal);
  });

  document.addEventListener('keydown', (e) => {
    if (e.key === 'Escape' && modalEl && !modalEl.hidden) {
      closeLegalModal();
    }
  });
});
