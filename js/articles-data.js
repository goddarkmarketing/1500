function getArticleBySlug(slug) {
  return ARTICLES.find((a) => a.slug === slug);
}

function getArticlesSorted() {
  return [...ARTICLES].sort((a, b) => new Date(b.date) - new Date(a.date));
}

function getFeaturedArticle() {
  return ARTICLES.find((a) => a.featured) || getArticlesSorted()[0];
}

function getPopularArticles(limit = 5) {
  const popular = ARTICLES.filter((a) => a.popular);
  const sorted = popular.length >= limit
    ? popular.sort((a, b) => new Date(b.date) - new Date(a.date))
    : getArticlesSorted();
  return sorted.slice(0, limit);
}

function getRecommendedArticles(currentSlug, limit = 5) {
  return getArticlesSorted()
    .filter((a) => a.slug !== currentSlug)
    .slice(0, limit);
}

function getArticlesByCategory(category) {
  if (!category || category === 'all') return getArticlesSorted();
  return getArticlesSorted().filter((a) => a.category === category);
}

function getCategoryMeta(category) {
  return ARTICLE_CATEGORIES[category] || { label: category, tagClass: '' };
}

function articleUrl(slug) {
  return `article.html?slug=${encodeURIComponent(slug)}`;
}
